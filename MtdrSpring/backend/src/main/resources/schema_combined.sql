-- =========================
-- ROLES
-- =========================
CREATE TABLE ADMIN.ROLES (
    ID_ROL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_ROL VARCHAR2(50) NOT NULL UNIQUE
);

-- =========================
-- USUARIOS
-- =========================
CREATE TABLE ADMIN.USUARIOS (
    ID_USUARIO  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TELEGRAM_ID NUMBER UNIQUE,
    NOMBRE      VARCHAR2(100) NOT NULL,
    USERNAME    VARCHAR2(100) NOT NULL,
    ID_ROL      NUMBER,
    FECHA_REGISTRO TIMESTAMP NOT NULL,
    CONSTRAINT FK_USUARIO_ROL FOREIGN KEY (ID_ROL)
        REFERENCES ADMIN.ROLES(ID_ROL)
);

-- =========================
-- PROYECTOS
-- =========================
CREATE TABLE ADMIN.PROYECTOS (
    ID_PROYECTO  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE       VARCHAR2(200) NOT NULL,
    DESCRIPCION  VARCHAR2(500),
    FECHA_INICIO TIMESTAMP,
    FECHA_FIN    TIMESTAMP,
    CODIGOACCESO VARCHAR2(8) UNIQUE
);

-- =========================
-- ESTADOS TAREA
-- =========================
CREATE TABLE ADMIN.ESTADOS_TAREA (
    ID_ESTADO    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_ESTADO VARCHAR2(50) NOT NULL UNIQUE
);

-- =========================
-- PRIORIDADES
-- =========================
CREATE TABLE ADMIN.PRIORIDADES (
    ID_PRIORIDAD NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE       VARCHAR2(20) NOT NULL UNIQUE
);

-- =========================
-- MEDIOS
-- =========================
CREATE TABLE ADMIN.MEDIOS (
    ID_MEDIO    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_MEDIO VARCHAR2(50) NOT NULL UNIQUE
);

-- =========================
-- SPRINTS
-- (CAMBIO: se agrega FK ID_PROYECTO -> PROYECTOS, requerida por nueva BD)
-- =========================
CREATE TABLE ADMIN.SPRINTS (
    ID_SPRINT    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE       VARCHAR2(50) NOT NULL,
    FECHA_INICIO TIMESTAMP,
    FECHA_FIN    TIMESTAMP,
    ID_PROYECTO  NUMBER NOT NULL,
    CONSTRAINT FK_SPRINT_PROYECTO FOREIGN KEY (ID_PROYECTO)
        REFERENCES ADMIN.PROYECTOS(ID_PROYECTO)
);

-- =========================
-- TAREAS
-- (CAMBIO: se reemplaza ID_PROYECTO directo por ID_SPRINT como FK
--          y se agregan campos ELIMINADA, FECHA_ELIMINACION para borrado lógico)
-- =========================
CREATE TABLE ADMIN.TAREAS (
    ID_TAREA            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE              VARCHAR2(200) NOT NULL,
    DESCRIPCION         VARCHAR2(500),
    FECHA_CREACION      TIMESTAMP NOT NULL,
    FECHA_ENTREGA       TIMESTAMP,
    HORAS_ESTIMADAS     NUMBER,
    PUNTOS_HISTORIA     NUMBER,
    ID_ESTADO           NUMBER,
    ID_SPRINT           NUMBER NOT NULL,
    ID_USUARIO_ASIGNADO NUMBER,
    ID_PRIORIDAD        NUMBER,
    DELETED_BY          NUMBER,
    ELIMINADA           NUMBER(1) DEFAULT 0 NOT NULL,
    FECHA_ELIMINACION   TIMESTAMP,
    HORAS_REALES        NUMBER,
    CONSTRAINT FK_TAREA_ESTADO    FOREIGN KEY (ID_ESTADO)
        REFERENCES ADMIN.ESTADOS_TAREA(ID_ESTADO),
    CONSTRAINT FK_TAREA_SPRINT    FOREIGN KEY (ID_SPRINT)
        REFERENCES ADMIN.SPRINTS(ID_SPRINT),
    CONSTRAINT FK_TAREA_USUARIO   FOREIGN KEY (ID_USUARIO_ASIGNADO)
        REFERENCES ADMIN.USUARIOS(ID_USUARIO),
    CONSTRAINT FK_TAREA_PRIORIDAD FOREIGN KEY (ID_PRIORIDAD)
        REFERENCES ADMIN.PRIORIDADES(ID_PRIORIDAD),
    CONSTRAINT FK_DELETED_BY      FOREIGN KEY (DELETED_BY)
        REFERENCES ADMIN.USUARIOS(ID_USUARIO),
    CONSTRAINT CHK_PUNTOS_HISTORIA  CHECK (PUNTOS_HISTORIA > 0),
    CONSTRAINT CHK_HORAS_ESTIMADAS  CHECK (HORAS_ESTIMADAS > 0),
    CONSTRAINT CHK_HORAS_REALES     CHECK (HORAS_REALES >= 0),
    CONSTRAINT CHK_ELIMINADA        CHECK (ELIMINADA IN (0,1))
);

-- =========================
-- HISTORIAL DE ACCIONES
-- (REEMPLAZA la tabla KPIS del esquema original — mismo propósito,
--  renombrada y con campo ACCION extendido a 50 chars)
-- =========================
CREATE TABLE ADMIN.HISTORIAL_ACCIONES (
    ID_ACCION NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_MEDIO  NUMBER,
    ID_SPRINT NUMBER,
    ID_TAREA  NUMBER,
    ACCION    VARCHAR2(50),
    CONSTRAINT FK_HIST_MEDIO  FOREIGN KEY (ID_MEDIO)
        REFERENCES ADMIN.MEDIOS(ID_MEDIO),
    CONSTRAINT FK_HIST_SPRINT FOREIGN KEY (ID_SPRINT)
        REFERENCES ADMIN.SPRINTS(ID_SPRINT),
    CONSTRAINT FK_HIST_TAREA  FOREIGN KEY (ID_TAREA)
        REFERENCES ADMIN.TAREAS(ID_TAREA)
);

-- =========================
-- NOTIFICACIONES
-- =========================
CREATE TABLE ADMIN.NOTIFICACIONES (
    ID_NOTIFICACION NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_USUARIO      NUMBER,
    ID_TAREA        NUMBER,
    MENSAJE         VARCHAR2(300),
    FECHA_ENVIO     TIMESTAMP,
    LEIDA           NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT FK_NOTIF_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES ADMIN.USUARIOS(ID_USUARIO),
    CONSTRAINT FK_NOTIF_TAREA   FOREIGN KEY (ID_TAREA)
        REFERENCES ADMIN.TAREAS(ID_TAREA),
    CONSTRAINT CHK_NOTIF_LEIDA  CHECK (LEIDA IN (0,1))
);

-- =========================
-- TODOITEM (tabla legacy del proyecto original — se mantiene)
-- =========================
CREATE TABLE ADMIN.TODOITEM (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DESCRIPTION VARCHAR2(4000),
    CREATION_TS TIMESTAMP WITH TIME ZONE,
    DONE        NUMBER(1) DEFAULT 0
);

-- =========================
-- USERS (tabla legacy del proyecto original — para autenticación web)
-- =========================
CREATE TABLE ADMIN.USERS (
    ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME  VARCHAR2(100),
    PASSWORD  VARCHAR2(200),
    ROLE      VARCHAR2(50)
);

